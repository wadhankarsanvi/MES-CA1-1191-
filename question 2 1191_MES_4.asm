ORG 0000H
MOV 50H, #03H     
MOV 51H, #09H    

MOV R0, 50H
MOV R1, 51H
MOV 52H, #00H

LOOP:
MOV A, R0
JZ R0ZERO

MOV A, R1
JZ R1ZERO

DEC R0
DEC R1
SJMP LOOP

R0ZERO:
MOV A, R1
JZ EQUAL
MOV 52H, #0FFH
SJMP STOP

R1ZERO:
MOV 52H, #01H
SJMP STOP

EQUAL:
MOV 52H, #00H

STOP:
SJMP STOP

END
